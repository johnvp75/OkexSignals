package data;

public class CurrentBB {
	private double central;
	private double up;
	private double down;
	
	public CurrentBB(double central, double up, double down) {
		this.central = central;
		this.up = up;
		this.down = down;
	}

	/**
	 * @return the central
	 */
	public double getCentral() {
		return central;
	}

	/**
	 * @return the up
	 */
	public double getUp() {
		return up;
	}

	/**
	 * @return the down
	 */
	public double getDown() {
		return down;
	}
	
	

}
